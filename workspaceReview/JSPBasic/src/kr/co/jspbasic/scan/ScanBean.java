package kr.co.jspbasic.scan;

public class ScanBean {
	private int kor;
	private int eng;
	private int math;
	private int tot;
	private double avg;
	
	public ScanBean() {}

	public int getKor() {
		return kor;
	}

	public void setKor(int kor) {
		this.kor = kor;
	}

	public int getEng() {
		return eng;
	}

	public void setEng(int eng) {
		this.eng = eng;
	}

	public int getMath() {
		return math;
	}

	public void setMath(int math) {
		this.math = math;
	}

	public int getTot() {
		return tot;
	}

	public void setTot(int tot) {
		this.tot = tot;
	}

	public double getAvg() {
		return avg;
	}

	public void setAvg(double avg) {
		this.avg = Math.round(avg*100)/100.0;
	}
	
	
}
